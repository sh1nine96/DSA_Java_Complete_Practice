package com.shubham;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
//        primitive data types are the ones which cant be broken down further into another datatypes,
//        Lets say if we write String name = "Shubham"; we can break it into further individual characters
//        hence String is not a primitive datatype

        int rollNumber = 42;// by default integer values are int type hence we add L to long
        char letter = 'S'; // char only have single 'quotes' & String "Doubly quoted"
        float marksInDecimal = 95.85f; // we need to end float value with f
        double largeDecimalNumber = 67698696.967856474; // by default all decimal values are double hence we add f to float
        long largeIntegerValue = 8875484389649689L; // we need to suffix long values with L
        boolean check = true; // boolean only has true and false as its values
    }
}
